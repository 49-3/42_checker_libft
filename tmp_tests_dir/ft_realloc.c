#include "libft.h"

void	*ft_realloc(void *ptr, size_t newsize)
{
	char	*newptr;
	size_t	cursize;

	if (ptr == 0)
		return (malloc(newsize));
	cursize = sizeof(ptr);
	if (newsize <= cursize)
		return (ptr);
	newptr = ft_memalloc(newsize);
	if (!newptr)
		return (ptr);
	ft_memcpy(ptr, newptr, cursize);
	free(ptr);
	return (newptr);
}
